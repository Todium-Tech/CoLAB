#String Functions
name=input("Enter A String")
print("To Capitalize The First Character --> string.capitalize() --> ",name.capitalize())
print("To Convert String To Lower Case --> string.casefold() --> ",name.casefold())
print("Returns A Centered String --> string.centre() --> ",name.centre())
print("Enter A Word or Character To Know How Much Time It Occurs --> string.count(\"the element\")--> ",name.count(input("enter a Character To Count")))
